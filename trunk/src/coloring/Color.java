package coloring;

public class Color {
	private byte r;
	private byte g;
	private byte b;
	private byte a;
	public void setR(byte r){
		this.r=r;
	}
	public void setG(byte g){
		this.g=g;
	}
	public void setB(byte b){
		this.b=b;
	}
	public void setA(byte a){
		this.a=a;
	}
	public byte getR(){
		return r;
	}
	public byte getG(){
		return g;
	}
	public byte getB(){
		return b;
	}
	public byte getA(){
		return a;
	}
}
